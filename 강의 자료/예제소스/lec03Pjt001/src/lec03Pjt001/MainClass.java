package lec03Pjt001;

public class MainClass {

	public static void main(String[] args) {
		
		int i;	// 변수 선언
		i = 10;	// 변수 초기화
		System.out.println("i = " + i);
		
		int j = 20;	// 변수 선언&초기화
		System.out.println("j = " + j);
		
		System.out.println("=================================");
		
		int num = 0;
		System.out.println("num = " + num);
		
		num = 10;
		System.out.println("num = " + num);
		
		num = 100;
		System.out.println("num = " + num);
		
		num = 0;
		System.out.println("num = " + num);

	}

}
